


/*
TSR - COMMUNICATION AREA SECONDARY
*/ 


;(function(document,$) {


    window.tsrCommunicationSecondary = window.tsrCommunicationSecondary || {};

/////////////////////////////////////////////////////////////////////////////////////////////////////////
////// TSR - Init
/////////////////////////////////////////////////////////////////////////////////////////////////////////


    tsrCommunicationSecondary.tsrInit = function() {
       
         tsrCommunicationSecondary.tsrEqualHeights();
  
    };

/////////////////////////////////////////////////////////////////////////////////////////////////////////
////// TSR - Equal heights
/////////////////////////////////////////////////////////////////////////////////////////////////////////

	// Thanks Paul Irish
	$.fn.setAllToMaxHeight = function(){
		return this.height( Math.max.apply(this, $.map( this , function(e){ return $(e).height() }) ) );
	}


/////////////////////////////////////////////////////////////////////////////////////////////////////////
////// TSR - Equal heights
/////////////////////////////////////////////////////////////////////////////////////////////////////////

    tsrCommunicationSecondary.tsrEqualHeights = function () {


	    $('.tsr-section-communication-secondary').each(function () {

            var bw = $('body').width();
	        var el = $(this);

            if(bw >= 600){

                $('.tsr-section-communication-secondary p').css('height', 'auto').setAllToMaxHeight()﻿;
                $('.tsr-section-communication-secondary header').css('height', 'auto').setAllToMaxHeight()﻿;

            } else {

                $('.tsr-section-communication-secondary p').css('height', 'auto');
                $('.tsr-section-communication-secondary header').css('height', 'auto')﻿;

            } 

	      	
	    });

    };

  



/////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////

/////////////////////////////////////////////////////////////////////////////////////////////////////////
////// Ready
/////////////////////////////////////////////////////////////////////////////////////////////////////////

    $(document).on('ready', function(){

        tsrCommunicationSecondary.tsrInit();
      
    });


/////////////////////////////////////////////////////////////////////////////////////////////////////////
////// Resize
/////////////////////////////////////////////////////////////////////////////////////////////////////////


	// jquery.debouncing.js, thanks Paul Irish

    $(window).smartresize(function(){
  		tsrCommunicationSecondary.tsrEqualHeights();
	});

/////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////

})(document,jQuery);

